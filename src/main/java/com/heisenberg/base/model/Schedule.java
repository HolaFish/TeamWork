package com.heisenberg.base.model;

public class Schedule {
	private String id ; //唯一标识，可以不填，持久化时编辑数据时使用
	private String title ;  //显示在日历上的内容
	private int allDay;  //标识是否为全天，可以不填，调用event.allDay时会自动区分是否为全天
	private String start ;  //开始的时间，格式为 yyyy-MM-dd 或 yyyy-MM-dd HH:mm
	private String end ;//结束的时间，可以不填，格式为 yyyy-MM-dd 或 yyyy-MM-dd HH:mm
	private String url ;//可以不填，点击时跳转到指定url
	private String className;   //数据的样式，可以不填
	private String color ;  //背景和边框颜色，可以不填，默认为#3a87ad
	private String backgroundColor; //背景颜色，可以不填，默认为#3a87ad
	private String borderColor ;//边框颜色，可以不填，默认为#3a87ad
	private String textColor;   //文本颜色,可以不填，默认为白色
	private String userId;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
	public int getAllDay() {
		return allDay;
	}
	public void setAllDay(int allDay) {
		this.allDay = allDay;
	}
	public String getStart() {
		return start;
	}
	public void setStart(String start) {
		this.start = start;
	}
	public String getEnd() {
		return end;
	}
	public void setEnd(String end) {
		this.end = end;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getBackgroundColor() {
		return backgroundColor;
	}
	public void setBackgroundColor(String backgroundColor) {
		this.backgroundColor = backgroundColor;
	}
	public String getBorderColor() {
		return borderColor;
	}
	public void setBorderColor(String borderColor) {
		this.borderColor = borderColor;
	}
	public String getTextColor() {
		return textColor;
	}
	public void setTextColor(String textColor) {
		this.textColor = textColor;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	
	
}	
